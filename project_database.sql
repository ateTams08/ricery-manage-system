CREATE DATABASE project;
USE project;

CREATE TABLE PRODUCT(PROD_ID CHAR(4) NOT NULL PRIMARY KEY,
	PROD_BRAND VARCHAR(50) NOT NULL,
	PROD_TYPE VARCHAR(50) NOT NULL,
	PROD_WEIGHT DECIMAL(5,2) NOT NULL CHECK (PROD_WEIGHT > 0),
	PROD_QUANT INT NOT NULL CHECK (PROD_QUANT >= 0) DEFAULT 0,
    PROD_UPRICE DECIMAL(9,2) NOT NULL CHECK (PROD_UPRICE > 0));

CREATE TABLE EMPLOYEE(EMP_ID CHAR(3) NOT NULL PRIMARY KEY,
	EMP_FNAME VARCHAR(50) NOT NULL,
	EMP_LNAME VARCHAR(50) NOT NULL,
	EMP_GENDER ENUM('MALE', 'FEMALE'),
	EMP_CONTACTN CHAR(11) NOT NULL UNIQUE,
	EMP_EMAIL VARCHAR(254) NOT NULL UNIQUE,
	EMP_BANKACC VARCHAR(16) NOT NULL UNIQUE);

CREATE TABLE PRODUCT_EMPLOYEE (PROD_ID CHAR(4) NOT NULL,
    EMP_ID CHAR(3) NOT NULL,
    PRIMARY KEY (PROD_ID, EMP_ID),
    FOREIGN KEY (PROD_ID) REFERENCES PRODUCT(PROD_ID),
    FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE(EMP_ID));
    
CREATE TABLE SUPPLIER (SUPP_ID CHAR(5) NOT NULL PRIMARY KEY,
    EMP_ID CHAR(3) NOT NULL,
    SUPP_NAME VARCHAR(50) NOT NULL UNIQUE,
    SUPP_CONTACTN CHAR(11) NOT NULL UNIQUE,
    SUPP_ADD VARCHAR(254) NOT NULL,
    FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE(EMP_ID));

CREATE TABLE CUSTOMER (CUST_ID CHAR(5) NOT NULL PRIMARY KEY,
    EMP_ID CHAR(3) NOT NULL,
    CUST_FNAME VARCHAR(50) NOT NULL,
    CUST_LNAME VARCHAR(50) NOT NULL,
    CUST_CONTACTN CHAR(11) NOT NULL,
    FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE(EMP_ID));

CREATE TABLE PAYMENT(PAY_REFID VARCHAR(50) NOT NULL PRIMARY KEY,
	PAY_MODE ENUM('CASH', 'CARD', 'E-WALLET') NOT NULL,
	PAY_AMT DECIMAL(9,2) NOT NULL CHECK (PAY_AMT > 0));

CREATE TABLE PURCHASE (PURCH_ID CHAR(6) NOT NULL PRIMARY KEY,
	CUST_ID CHAR(5) NOT NULL,
    PAY_REFID VARCHAR(50) NOT NULL,
    PURCH_AMT DECIMAL(9,2) NOT NULL CHECK (PURCH_AMT > 0),
    PURCH_DATE DATETIME NOT NULL,
    FOREIGN KEY (CUST_ID) REFERENCES CUSTOMER(CUST_ID),
    FOREIGN KEY (PAY_REFID) REFERENCES PAYMENT(PAY_REFID));
    
CREATE TABLE PURCHASE_PRODUCT (PURCH_ID CHAR(6) NOT NULL,
    PROD_ID CHAR(4) NOT NULL,
    PRIMARY KEY (PURCH_ID, PROD_ID),
    PUR_PRO_QUANT INT NOT NULL CHECK (PUR_PRO_QUANT > 0),
    PUR_PRO_WEIGHT DECIMAL(5,2) NOT NULL CHECK (PUR_PRO_WEIGHT > 0),
    PUR_PRO_UPRICE DECIMAL(9,2) NOT NULL CHECK (PUR_PRO_UPRICE > 0),
    PUR_PRO_AMT DECIMAL(9,2) AS (PUR_PRO_QUANT * PUR_PRO_WEIGHT * PUR_PRO_UPRICE) STORED CHECK (PUR_PRO_AMT > 0),
    FOREIGN KEY (PURCH_ID) REFERENCES PURCHASE(PURCH_ID),
    FOREIGN KEY (PROD_ID) REFERENCES PRODUCT(PROD_ID));
    
